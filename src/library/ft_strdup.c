#include "../../includes/libft.h"

char	*ft_strdup(const char *s)
{
	char	*s2;
	size_t	i;

	i = 0;
	s2 = malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!s2)
		return (0);
	while (s[i] != '\0')
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
